    public void mostrarMatrizA(int m[][], int n) {
        DefaultTableModel model = (DefaultTableModel) tblmatrizA.getModel();
        model.setRowCount(n);
        model.setColumnCount(n);

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                tblmatrizA.setValueAt(m[i][j], i, j);
            }
        }
    }

