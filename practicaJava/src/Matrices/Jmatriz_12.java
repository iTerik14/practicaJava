    public int[][] llenarMatrizA(int n) {
        int m[][] = new int[n][n];
        int a=0;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                m[i][j] = a++;
            }
        }
        return m;
    }

    public int[][] llenarMatrizB(int n) {
        int m[][] = new int[n][n];
        int a=1;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                m[i][j] = a++;
            }
        }
        return m;
    }

    public void mostrarMatrizSuma(int m[][], int n) {
        DefaultTableModel model = (DefaultTableModel) tblmatrizR.getModel();
        model.setRowCount(n);
        model.setColumnCount(n);

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                tblmatrizR.setValueAt(m[i][j], i, j);
            }
        }
    }

    public int[][] sumaMatriz(int mA[][], int mB[][], int n) {
        int mR[][] = new int[n][n];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                mR[i][j] = mA[i][j] + mB[i][j];
            }
        }
        return mR;
    }


